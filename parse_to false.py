# Пустая строка
if not "":
    print("Пустая строка распарсилась в False")

# Пустой список
if not []:
    print("Пустой список распарсился в False")

# Пустой кортеж
if not ():
    print("Пустой кортеж распарсился в False")

# Пустой словарь
if not {}:
    print("Пустой словарь распарсился в False")

# Пустое множество
if not set():
    print("Пустое множество распарсилось в False")

# Число 0
if not 0:
    print("Число 0 распарсилось в False")

# Число 0.0
if not 0.0:
    print("Число 0.0 распарсилось в False")

# None
if not None:
    print("None распарсилось в False")

# Логическое False
if not False:
    print("Логическое False распарсилось в False")
